/**
 * Generates a pass-phrase with the EFF's dice generated
 * word list
 *
 * https://www.eff.org/dice
 */
pub fn gen_phrase(count: usize) -> String {
    let mut phrase = vec![];

    let mut rng = rand::thread_rng();
    let mut get_index = || {
        let mut pos = 1;
        let mut res = 0;
        for _i in 0..5 {
            res += rng.gen_range(1, 6) * pos;
            pos *= 10;
        }
        res
    };

    for _i in 0..count {
        let y: u32 = get_index();
        phrase.push(*WORDS.get(&y).unwrap());
    }

    phrase.join("-")
}

